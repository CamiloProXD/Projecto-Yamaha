public class Sede {
    private int idSede;
    private String municipio;
    private int inventario_id;
    private String nombreSede;
    private String direccionSede;
    private int idAdministrador;

    public Sede(int idSede, String municipio, int inventario_id, String nombreSede, String direccionSede, int idAdministrador) {
        this.idSede = idSede;
        this.municipio = municipio;
        this.inventario_id = inventario_id;
        this.nombreSede = nombreSede;
        this.direccionSede = direccionSede;
        this.idAdministrador = idAdministrador;
    }

    public Sede() {
    }

    public int getIdSede() {
        return idSede;
    }

    public void setIdSede(int idSede) {
        this.idSede = idSede;
    }

    public String getMunicipio() {
        return municipio;
    }

    public void setMunicipio(String municipio) {
        this.municipio = municipio;
    }

    public int getInventario_id() {
        return inventario_id;
    }

    public void setInventario_id(int inventario_id) {
        this.inventario_id = inventario_id;
    }

    public String getNombreSede() {
        return nombreSede;
    }

    public void setNombreSede(String nombreSede) {
        this.nombreSede = nombreSede;
    }

    public String getDireccionSede() {
        return direccionSede;
    }

    public void setDireccionSede(String direccionSede) {
        this.direccionSede = direccionSede;
    }

    public int getIdAdministrador() {
        return idAdministrador;
    }

    public void setIdAdministrador(int idAdministrador) {
        this.idAdministrador = idAdministrador;
    }

    
    
  
}
